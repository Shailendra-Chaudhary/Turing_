
#include<bits/stdc++.h>
using namespace std;

void displayFactors()
{
    cout << "Factors to consider when choosing hardware for embedded medical devices: \n";
    cout << "1. **Safety and Reliability:** \n";
    cout << "   - Medical devices must operate reliably and without failure in life-critical environments. \n";
    cout << "   - Choose hardware from reputable manufacturers with a strong track record in medical applications. \n";

    cout << "2. **Safety Standards Compliance:** \n";
    cout << "   - Embedded medical devices must adhere to strict safety standards like ISO 13485, IEEE 802.11, and 510(k).\n";
    cout << "   - Ensure that the hardware meets these standards to avoid legal and regulatory issues. \n";

    cout << "3. **Precision and Accuracy:** \n";
    cout << "   - Medical devices often require high-precision sensors and computations. \n";
    cout << "   - Choose hardware with appropriate ADC, DAC, and CPU capabilities to meet precision requirements. \n";

    cout << "4. **Power Efficiency:** \n";
    cout << "   - Medical devices typically operate for long periods with limited battery life. \n";
    cout << "   - Opt for power-efficient hardware components to prolong device lifespan and reduce costs. \n";

    cout << "5. **Form Factor and Size:** \n";
    cout << "   - Medical devices must fit into various sizes, from small implantable devices to larger monitors. \n";
    cout << "   - Consider the form factor and size requirements of the specific device and choose hardware accordingly. \n";

    cout << "6. **Interoperability:** \n";
    cout << "   - Ensure that the hardware can interact with other medical devices, systems, and databases seamlessly. \n";
    cout << "   - Choose hardware with appropriate communication protocols and interfaces. \n";

    cout << "7. **Security:** \n";
    cout << "   - Medical devices store sensitive patient data, so ensure that the hardware has robust security measures. \n";
    cout << "   - Implement encryption, access controls, and data protection mechanisms to safeguard patient information. \n";

    cout << "8. **Real-Time Performance:** \n";
    cout << "   - Some medical devices require real-time data processing and response times. \n";
    cout << "   - Choose hardware with a real-time operating system and appropriate CPU capabilities to meet these requirements. \n";

    cout << "9. **Cost:** \n";
    cout << "   - While safety and reliability are crucial, consider the cost of the hardware. \n";
    cout << "   - Choose hardware that offers a good balance between features and cost. \n";

    cout << "10. **Debugging and Support:** \n";
    cout << "   - Ensure that the hardware has good debugging tools and support from the manufacturer. \n";
    cout << "   - Reliable debugging and support can help identify and resolve issues promptly. \n";

}


int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    
    cout << "\n\n**Program to Display Factors Considered in Choosing Hardware for Embedded Medical Devices**\n\n";
    displayFactors();
    
    cout << "\n\n";
    return 0;
} 
 
